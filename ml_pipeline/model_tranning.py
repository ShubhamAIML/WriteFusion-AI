# ════════════════════════════════════════════════════
#  CUSTOM TOKENIZER — fixes apostrophe splitting bug
# ════════════════════════════════════════════════════
class SimpleWordTokenizer:
    """
    Drop-in replacement for Keras Tokenizer that NEVER splits
    apostrophe-words. "don't" and "mom's" are always single tokens.

    Provides the same interface used downstream:
        .fit_on_texts(lines)
        .texts_to_sequences(lines)
        .word_index   (dict: word -> id)
        .index_word    (dict: id -> word)
    """
    # Matches a run of letters, optionally followed by an
    # apostrophe + more letters — e.g. "don't", "mom's", "y'all"
    WORD_RE = re.compile(r"[a-z]+(?:'[a-z]+)?")

    def __init__(self, num_words=None, oov_token='<OOV>'):
        self.num_words  = num_words
        self.oov_token  = oov_token
        self.word_index = {}
        self.index_word = {}
        self.word_counts = {}

    def _tokenize(self, text):
        return self.WORD_RE.findall(text.lower())

    def fit_on_texts(self, lines):
        counter = Counter()
        for line in lines:
            counter.update(self._tokenize(line))

        self.word_counts = dict(counter)

        # Reserve index 0 for padding, 1 for OOV
        self.word_index = {self.oov_token: 1}
        self.index_word = {1: self.oov_token}

        most_common = counter.most_common(
            None if self.num_words is None else self.num_words - 2
        )
        for i, (word, _) in enumerate(most_common, start=2):
            self.word_index[word] = i
            self.index_word[i]    = word

    def texts_to_sequences(self, lines):
        oov_id = self.word_index.get(self.oov_token, 1)
        limit  = self.num_words if self.num_words else len(self.word_index) + 2
        out = []
        for line in lines:
            tokens = self._tokenize(line)
            ids = [
                self.word_index.get(t, oov_id) if self.word_index.get(t, oov_id) < limit
                else oov_id
                for t in tokens
            ]
            out.append(ids)
        return out


