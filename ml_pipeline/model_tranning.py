#  STEP 4 — TRANSFORMER MODEL (unchanged from v2)
# ════════════════════════════════════════════════════
class PositionalEmbedding(layers.Layer):
    def __init__(self, seq_len, vocab_size, embed_dim, **kwargs):
        super().__init__(**kwargs)
        self.seq_len    = seq_len
        self.vocab_size = vocab_size
        self.embed_dim  = embed_dim
        self.token_emb  = layers.Embedding(vocab_size, embed_dim, mask_zero=True)
        self.pos_emb    = layers.Embedding(seq_len, embed_dim)

    def call(self, x):
        positions = tf.range(start=0, limit=tf.shape(x)[-1], delta=1)
        return self.token_emb(x) + self.pos_emb(positions)

    def compute_mask(self, inputs, mask=None):
        return self.token_emb.compute_mask(inputs)

    def get_config(self):
        cfg = super().get_config()
        cfg.update({'seq_len': self.seq_len, 'vocab_size': self.vocab_size,
                    'embed_dim': self.embed_dim})
        return cfg


class TransformerBlock(layers.Layer):
    def __init__(self, embed_dim, num_heads, ff_dim, dropout, **kwargs):
        super().__init__(**kwargs)
        self.embed_dim = embed_dim
        self.num_heads = num_heads
        self.ff_dim    = ff_dim
        self.dropout   = dropout

        self.att  = layers.MultiHeadAttention(num_heads=num_heads, key_dim=embed_dim // num_heads)
        self.ffn  = tf.keras.Sequential([
            layers.Dense(ff_dim, activation='gelu'),
            layers.Dense(embed_dim),
        ])
        self.norm1 = layers.LayerNormalization(epsilon=1e-6)
        self.norm2 = layers.LayerNormalization(epsilon=1e-6)
        self.drop1 = layers.Dropout(dropout)
        self.drop2 = layers.Dropout(dropout)

    def call(self, x, training=False):
        seq_len = tf.shape(x)[1]
        causal_mask = tf.linalg.band_part(tf.ones((seq_len, seq_len)), -1, 0)
        causal_mask = tf.cast(causal_mask, tf.bool)

        attn_out = self.att(x, x, attention_mask=causal_mask, training=training)
        x = self.norm1(x + self.drop1(attn_out, training=training))

        ffn_out = self.ffn(x)
        x = self.norm2(x + self.drop2(ffn_out, training=training))
        return x

    def get_config(self):
        cfg = super().get_config()
        cfg.update({'embed_dim': self.embed_dim, 'num_heads': self.num_heads,
                    'ff_dim': self.ff_dim, 'dropout': self.dropout})
        return cfg


class LastToken(layers.Layer):
    """Picks the last timestep's representation from a sequence.
    A named custom layer instead of a raw Lambda — Lambda layers
    with Python functions are blocked from safe deserialization by
    Keras when the saved model is reloaded elsewhere (e.g. in a
    Flask app), so this is required for the model to load later."""
    def call(self, x):
        return x[:, -1, :]

    def get_config(self):
        return super().get_config()


def build_model(vocab_size, seq_len, embed_dim, num_heads, num_layers, ff_dim, dropout):
    inp = layers.Input(shape=(seq_len,), name='input')
    x = PositionalEmbedding(seq_len, vocab_size, embed_dim, name='pos_embedding')(inp)
    x = layers.Dropout(dropout)(x)

    for i in range(num_layers):
        x = TransformerBlock(embed_dim, num_heads, ff_dim, dropout,
                             name=f'transformer_block_{i}')(x)

    x = LastToken(name='last_token')(x)
    x = layers.Dense(embed_dim, activation='gelu', name='proj')(x)
    x = layers.Dropout(dropout)(x)
    out = layers.Dense(vocab_size, activation='softmax', dtype='float32', name='output')(x)

    return Model(inputs=inp, outputs=out)


