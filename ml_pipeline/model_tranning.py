def load_corpus(path):
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"\n[!] File not found: {path}")

    lines = [l.strip() for l in p.read_text(encoding='utf-8').splitlines()
             if l.strip() and len(l.strip().split()) >= 4]

    total_words = sum(len(l.split()) for l in lines)
    print(f"\n{'='*52}")
    print(f"STEP 1: CORPUS")
    print(f"{'='*52}")
    print(f"  Sentences    : {len(lines):,}")
    print(f"  Total words  : {total_words:,}")
    print(f"  Avg length   : {total_words/max(len(lines),1):.1f} words/sentence")

    print(f"\n  Sample lines:")
    for l in lines[:3]:
        print(f"    • {l[:80]}")
    return lines


# ════════════════════════════════════════════════════
#  STEP 2 — TOKENIZE (custom, apostrophe-safe)
# ════════════════════════════════════════════════════
def build_tokenizer(lines, vocab_size):
    tok = SimpleWordTokenizer(num_words=vocab_size, oov_token='<OOV>')
    tok.fit_on_texts(lines)
    actual_vocab = min(vocab_size, len(tok.word_index) + 1)

    print(f"\n{'='*52}")
    print(f"STEP 2: TOKENIZATION")
    print(f"{'='*52}")
    print(f"  Unique words  : {len(tok.word_index):,}")
    print(f"  Vocab used    : {actual_vocab:,}")

    # Sanity check — confirm apostrophe words survive intact
    apostrophe_words = [w for w in tok.word_index if "'" in w][:10]
    print(f"  Apostrophe words preserved (sample): {apostrophe_words}")

    return tok, actual_vocab


# ════════════════════════════════════════════════════
#  STEP 3 — SEQUENCES
# ════════════════════════════════════════════════════
def create_sequences(lines, tokenizer, seq_len):
    X_list, y_list = [], []
    oov_id  = tokenizer.word_index.get('<OOV>', 1)
    skipped = 0

    sequences = tokenizer.texts_to_sequences(lines)

    for ids in sequences:
        if not ids or len(ids) < seq_len + 1:
            skipped += 1
            continue
        if ids.count(oov_id) / len(ids) > 0.30:
            skipped += 1
            continue
        for i in range(seq_len, len(ids)):
            X_list.append(ids[i - seq_len : i])
            y_list.append(ids[i])

    X = np.array(X_list, dtype=np.int32)
    y = np.array(y_list, dtype=np.int32)

    ram_mb = (X.nbytes + y.nbytes) / 1e6
    print(f"\n{'='*52}")
    print(f"STEP 3: SEQUENCES")
    print(f"{'='*52}")
    print(f"  Sequences     : {len(X):,}")
    print(f"  Skipped lines : {skipped:,}")
    print(f"  X shape       : {X.shape}")
    print(f"  RAM used      : ~{ram_mb:.1f} MB ✓")
    return X, y


