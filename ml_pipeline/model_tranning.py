    ax[0].plot(history.history['val_loss'], label='Val Loss',   color='#38d9a9')
    ax[0].set_title('Loss'); ax[0].set_xlabel('Epoch'); ax[0].legend(); ax[0].grid(alpha=.3)

    ax[1].plot(history.history['accuracy'],     label='Train Acc', color='#5b6ef5')
    ax[1].plot(history.history['val_accuracy'], label='Val Acc',   color='#38d9a9')
    ax[1].set_title('Accuracy'); ax[1].set_xlabel('Epoch'); ax[1].legend(); ax[1].grid(alpha=.3)

    plt.tight_layout()
