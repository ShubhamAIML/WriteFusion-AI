def main():
    os.makedirs(SAVE_DIR, exist_ok=True)

    lines = load_corpus(CORPUS_FILE)
    tokenizer, actual_vocab = build_tokenizer(lines, VOCAB_SIZE)

    tok_path = os.path.join(SAVE_DIR, 'tokenizer.pkl')
    with open(tok_path, 'wb') as f:
        pickle.dump(tokenizer, f)
    print(f"  Tokenizer saved : {tok_path}")

    X, y = create_sequences(lines, tokenizer, SEQ_LEN)

    print(f"\n{'='*52}")
    print(f"STEP 4: MODEL  (Transformer decoder, GPT-style)")
    print(f"{'='*52}")
    model = build_model(
        vocab_size = actual_vocab, seq_len = SEQ_LEN, embed_dim = EMBED_DIM,
        num_heads  = NUM_HEADS, num_layers = NUM_LAYERS, ff_dim = FF_DIM,
        dropout    = DROPOUT_RATE,
    )
    model.compile(
        optimizer = tf.keras.optimizers.AdamW(learning_rate=0.001, weight_decay=1e-4, clipnorm=1.0),
        loss      = 'sparse_categorical_crossentropy',
        metrics   = ['accuracy']
    )
    model.summary()
    print(f"\n  Parameters   : {model.count_params():,}")

    print(f"\n{'='*52}")
    print(f"STEP 5: TRAINING")
    print(f"{'='*52}")
    print(f"  Sequences    : {len(X):,}")
    print(f"  Vocab size   : {actual_vocab:,}")
    print(f"  Seq length   : {SEQ_LEN}")
    print(f"  Heads/Layers : {NUM_HEADS} heads, {NUM_LAYERS} layers")
    print(f"  Max epochs   : {EPOCHS}  (EarlyStopping patience=5)")

    history = model.fit(
        X, y, batch_size=BATCH_SIZE, epochs=EPOCHS,
        validation_split=VALIDATION_SPLIT,
        callbacks=get_callbacks(SAVE_DIR), verbose=1,
    )

    model_path = os.path.join(SAVE_DIR, 'best_model.keras')
    model.save(model_path)

    cfg = {
        'vocab_size': actual_vocab, 'seq_len': SEQ_LEN, 'embed_dim': EMBED_DIM,
        'num_heads': NUM_HEADS, 'num_layers': NUM_LAYERS, 'ff_dim': FF_DIM,
    }
    cfg_path = os.path.join(SAVE_DIR, 'model_config.json')
    with open(cfg_path, 'w') as f:
        json.dump(cfg, f, indent=2)

    plot_history(history, SAVE_DIR)
    inference_demo(model, tokenizer, SEQ_LEN)

    best_acc   = max(history.history['val_accuracy'])
    best_loss  = min(history.history['val_loss'])
    perplexity = np.exp(best_loss)
    epochs_run = len(history.history['loss'])

    print(f"\n{'='*52}")
    print(f"TRAINING COMPLETE")
    print(f"{'='*52}")
    print(f"  Epochs run      : {epochs_run}/{EPOCHS}")
    print(f"  Best val loss   : {best_loss:.4f}")
    print(f"  Best val acc    : {best_acc*100:.2f}%")
    print(f"  Perplexity      : {perplexity:.2f}")
    print(f"  Random chance   : {1/actual_vocab*100:.4f}%")

    try:
        from google.colab import files
        files.download(model_path)
        files.download(tok_path)
        files.download(cfg_path)
        print(f"\n✓ Downloads started!")
    except Exception:
        print(f"\n  Manual download: Files panel → {SAVE_DIR}")


main()