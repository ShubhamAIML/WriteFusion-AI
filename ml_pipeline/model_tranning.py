# ════════════════════════════════════════════════════
#  STEP 6 — INFERENCE
# ════════════════════════════════════════════════════
def predict_next(model, tokenizer, seed_text, seq_len, top_k=5, temperature=0.8):
    ids   = tokenizer.texts_to_sequences([seed_text])[0][-seq_len:]
    ids   = pad_sequences([ids], maxlen=seq_len, padding='pre')
    probs = model.predict(ids, verbose=0)[0].astype('float64')

    probs = np.log(probs + 1e-10) / temperature
    probs = np.exp(probs)
    probs /= probs.sum()

    top   = np.argsort(probs)[::-1]
    top   = [i for i in top if i > 1][:top_k]

    return [(tokenizer.index_word.get(i, '?'), round(float(probs[i]), 4)) for i in top]


def generate_text(model, tokenizer, seed_text, seq_len, max_words=25, temperature=0.8):
    text = seed_text
    for _ in range(max_words):
        ids = tokenizer.texts_to_sequences([text])[0][-seq_len:]
        ids = pad_sequences([ids], maxlen=seq_len, padding='pre')

        probs = model.predict(ids, verbose=0)[0]
        probs = np.log(probs + 1e-10) / temperature
        probs = np.exp(probs)
        probs /= probs.sum()

        next_id = np.random.choice(len(probs), p=probs)
        if next_id <= 1:
            continue

        word = tokenizer.index_word.get(next_id)
        if word:
            text += " " + word
    return text


def inference_demo(model, tokenizer, seq_len):
    test_phrases = [
        "it was the best of",
        "she looked at him and",
        "the sun was setting over",
        "he could not believe what",
        "they walked together through the",
        "i don't know what",
    ]
    print(f"\n{'='*52}")
    print(f"INFERENCE DEMO — Top 5 next-word predictions")
    print(f"{'='*52}")
    for phrase in test_phrases:
        preds = predict_next(model, tokenizer, phrase, seq_len, top_k=5, temperature=0.8)
        pstr  = "  |  ".join(f"{w} ({p})" for w, p in preds)
        print(f"\n  Input : '{phrase}'")
        print(f"  →  {pstr}")

    print(f"\n{'='*52}")
    print(f"TEXT GENERATION DEMO")
    print(f"{'='*52}")
    seed = "it was the best of"
    generated = generate_text(model, tokenizer, seed, seq_len, max_words=25, temperature=0.8)
    print(f"\n  Seed      : {seed}")
    print(f"  Generated : {generated}")


